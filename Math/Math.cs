namespace Math;

public static class Math
{
    public static double PI
    {
        get { return (3.142); }
    }

    public static long AddNums(long num1, long num2)
    {
        return num1 + num2;
    }

    public static long SubtractNums(long num1, long num2)
    {
        return num1 - num2;
    }

    public static long MultiplyNums(long num1, long num2)
    {
        return num1 * num2;
    }

    public static long DivideNums(long num1, long num2)
    {
        return num1 / num2;
    }
}